import re
from typing import Optional

_INTENT_KEYWORDS: list[tuple[str, str]] = [
    (r"куда\s+плат",             "Клиент спрашивает куда переводить оплату"),
    (r"как\s+оплатить",          "Клиент готов оплатить"),
    (r"как\s+купить",            "Клиент хочет купить"),
    (r"хочу\s+купить",           "Клиент хочет купить"),
    (r"хочу\s+заказать",         "Клиент хочет оформить заказ"),
    (r"оформить\s+заказ",        "Клиент хочет оформить заказ"),
    (r"сделать\s+заказ",         "Клиент хочет оформить заказ"),
    (r"какая\s+цена",            "Клиент уточняет цену"),
    (r"сколько\s+стоит",         "Клиент спрашивает стоимость"),
    (r"сколько\s+стоят",         "Клиент спрашивает стоимость"),
    (r"готов\s+купить",          "Клиент готов к покупке"),
    (r"готов\s+заказать",        "Клиент готов к заказу"),
    (r"готов\s+платить",         "Клиент готов к оплате"),
    (r"реквизиты",               "Клиент запрашивает реквизиты для оплаты"),
    (r"номер\s+карты",           "Клиент запрашивает карту для оплаты"),
    (r"куда\s+перевести",        "Клиент готов перевести деньги"),
    (r"куда\s+скинуть",          "Клиент готов перевести деньги"),
    (r"\bберу\b",                "Клиент решил купить"),
    (r"\bоплачу\b",              "Клиент готов оплатить"),
]

_COMPILED: list[tuple[re.Pattern, str]] = [
    (re.compile(pattern, re.IGNORECASE), summary)
    for pattern, summary in _INTENT_KEYWORDS
]


def detect_intent(message: str) -> tuple[bool, Optional[str]]:
    for pattern, summary in _COMPILED:
        if pattern.search(message):
            return True, summary
    return False, None
